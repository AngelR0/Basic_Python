# This file contains further information about variables

"""
Basic data types: Numbers. Strings, Booleans, Sequences, Dictionaries.

    Numbers:
        Integers : int example (2)
        Floats : float example (13.3)
    
    Strings: str example ("This is a string")
    
    Booleans: bool example (True)
    
    Lists: list example ([1, 2, 3])
    
    Sequences: tuples example (("a", "b", "c"))
    
    Dictionarys: dict example ({"a": 1, "b": 2, "c": 3})
    
"""

myint = 3
myfloat = 3.3
mystr = "Hello, world!"
mybool = True
mylist = [1, 2, 3, "four", 4.5, False]  # list can contain mixed data types
mytuple = (0, 1, 2)   # tuples are immutable (can't change, after creation)
# dictionaries are used map unique keys to specific values
mydict = {"a": 1, "b": 2, "c": 3}

print(myint, "\n")      # Refresher: `\n` creates a new line.
print(myfloat, "\n")
print(mystr, "\n")
print(mybool, "\n")
print(mylist, "\n")
print(mytuple, "\n")
print(mydict, "\n")
